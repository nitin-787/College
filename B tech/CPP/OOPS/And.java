public class And {
    public static void main(String[] args) {
        // Example values
        int num1 = 224;  // Binary:  00001010
        int num2 = 64;  // Binary: 10000000

        // Bitwise AND operation
        int result = num1 & num2;

        // Display the result
        System.out.println("Result of bitwise AND: " + result);
    }
}
